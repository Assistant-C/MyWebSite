const draggables = document.querySelectorAll('.ditem');
const dropzones = document.querySelectorAll('.dropzone');

draggables.forEach(draggable => {
    draggable.addEventListener('dragstart', () => {
        draggable.classList.add('dragging');
    });

    draggable.addEventListener('dragend', () => {
        draggable.classList.remove('dragging');
    });
});

dropzones.forEach(dropzone => {
    dropzone.addEventListener('dragover', e => {
        e.preventDefault();
        dropzone.classList.add('over');
    });

    dropzone.addEventListener('dragleave', () => {
        dropzone.classList.remove('over');
    });

    dropzone.addEventListener('drop', e => {
        e.preventDefault();
        const draggable = document.querySelector('.dragging');
        dropzone.appendChild(draggable);
        dropzone.classList.remove('over');
    });
});

function checkAnswers() {
    const customerSays = document.getElementById('customer-says').children;
    const supplierSays = document.getElementById('supplier-says').children;

    const customerCorrect = ["I appreciate your help.", "You'd really be helping us.", "I need a favour.", "I promise this won't become the norm."];
    const supplierCorrect = ["I think we can make an exception this time.", "I'm happy to help you.", "I'm not sure if I can do that.", "Let me see what I can do."];

    let customerCorrectCount = 0;
    let supplierCorrectCount = 0;

    for (let i = 0; i < customerSays.length; i++) {
        if (customerCorrect.includes(customerSays[i].textContent)) {
            customerCorrectCount++;
        }
    }

    for (let i = 0; i < supplierSays.length; i++) {
        if (supplierCorrect.includes(supplierSays[i].textContent)) {
            supplierCorrectCount++;
        }
    }

    alert(`Customer correct answers: ${customerCorrectCount} out of ${customerCorrect.length}\nSupplier correct answers: ${supplierCorrectCount} out of ${supplierCorrect.length}`);
}